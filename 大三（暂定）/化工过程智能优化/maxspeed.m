%最速下降法
%通过负梯度方向做迭代
%这里可以不通过一维优化求出最优迭代步长
%lambda=g'*g/(g'*H*g)
%缺点：在接近优化点是出现锯齿现象，移动步长小，收敛慢
%通常与其他方法结合（一般是牛顿法），在接近优化点时使用

clc;clear;

epsilon=0.00000001
x=[0;3]


f=@(x) (x(1)-2)^4+(x(1)-2*x(2))^2;
g=@(x) [4*(x(1)-2)^3+2*x(1)-4*x(2);-4*x(1)+8*x(2)];
h=@(x) [12*(x(1)-2)^2+2,-4;-4,8];

while norm(g(x))>epsilon
    n=norm(g(x))
    p=-g(x);
    lambda=g(x)'*g(x)/(g(x)'*h(x)*g(x));
    x=x+lambda*p;

end
x
f(x)
